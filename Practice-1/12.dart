//Suppose, your distance to office from home is 25 km
//and you travel 40 km per hour.
//Write a program to calculate time taken to reach office in minutes.
//Formula= (distance) / (speed)
void main() {
  num distanceAndSpeed = 25 / 40;
  num CalculatedMinutes = distanceAndSpeed * 60;
  print("$CalculatedMinutes Minutes");
}